    <h4>¿Que es GNUno?</h4>
    <p>
        <strong>GNUno</strong> comenzó con un grupo de compañeros de la carrera de Informática que quisimos organizarnos para que
        la Universidad Nacional del Oeste sea sede de la FLISoL (Festival Latinoamericano de Instalación de Software Libre)
        en el 2016. Por falta de tiempo no pudo ser, pero se armó un grupo interesante de gente y se fueron sumando adeptos
        con ganas de trabajar, compartir, aprender y enseñar.
    </p>
    <h4>¿Cuales son los objetivos?</h4>
    <p>
        Hoy nuestro objetivo va más allá de dar a conocer y promover el uso del software libre y GNU/Linux, sino también capacitar,
        dar talleres, seminarios, charlas, traer invitados, realizar hackatones y mas. La idea es complementar el contenido
        de la carrera con diversas actividades sobre el campo de la informática, yendo de tópicos de nivel introductorio
        hasta avanzados si se da la chance ¡Siempre que haya alguien que quiera compartir su conocimiento siempre va a haber
        compañeros dispuestos a aprender!
    </p>
    <h4>¿Donde puedo enterarme de las novedades?</h4>
    <p>
        Nuestra vía principal de comunicación es la
        <strong>lista de correos</strong>, también se compartirá información relevante sobre eventos en el grupo de Facebook
        <font
            style="font-weight:bolder">Universidad Nacional del Oeste - Licenciatura en Informática</font> y en la página de Facebook
            <strong>UNO Informática</strong>. También en el
            <strong>Foro No-Oficial de la UNO</strong> hay un sub-foro dedicado para compartir información de manera persistente.
    </p>
    <h4>¿Como participar?</h4>
    <p>
        En la página vas a encontrar las instrucciones para unirte a la lista de correos y si queres participar, sugerir u organizar
        en cualquiera de las actividades solo tenes que acercarte, todos son bienvenidos.
    </p>
